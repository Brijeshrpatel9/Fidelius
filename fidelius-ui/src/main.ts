import './polyfills';

import { enableProdMode } from '@angular/core';
import { platformBrowserDynamic } from '@angular/platform-browser-dynamic';

import { AppModule } from './app/app.module';
import { environment } from './environments/environment';
import {CookieService} from 'ngx-cookie-service';

if (environment.production) {
  enableProdMode();
}
const FIRST_TIME: string = 'FIDELIUS_FIRST_TIME_LOAD';
let _cookieService: CookieService = new CookieService(document);
// To get around CT, check to see if this is a first time load
if (!_cookieService.check(FIRST_TIME)) {
  _cookieService.set(FIRST_TIME, 'no');
  location.reload();
}

platformBrowserDynamic().bootstrapModule(AppModule).then(ref => {
  // Ensure Angular destroys itself on hot reloads.
  if (window['ngRef']) {
    window['ngRef'].destroy();
  }
  window['ngRef'] = ref;

  // Otherise, log the boot error
}).catch(err => console.error(err));

